class Byte
{
	public static void main(String[] args)
	{
		byte noFloors =4;
		byte staff =30;
		byte subjects =10;
		byte noRooms =12;
		byte batches =15;
		byte lifts =2;
		System.out.println("noFloors : "+noFloors);
		System.out.println("staff : "+staff);
		System.out.println("subjects : "+subjects);
		System.out.println("noRooms : "+noRooms);
		System.out.println("batches : "+batches);
		System.out.println("lifts : "+lifts);
		
	}
}