class DoubleExample
{
	public static void main(String[] args)
	{
		double lightSpeed = 3.0e8;
		double distSunToEarth = 1.5e8d;
		double distEarthToMoon = 3.84e8;
		double tempSun = 1.5e7d; //15 million degree celcius
		double soundSpeed = 12.773042253875d; 
		System.out.println("light speed : "+lightSpeed+"m/s");
		System.out.println("distance between sun to earth : "+distSunToEarth+"km");
		System.out.println("distance between moon to earth : "+distEarthToMoon+"m");
		System.out.println("tempSun : "+tempSun+"degree celcius //15 million degree celcius");
		System.out.println("light soundSpeed : "+soundSpeed+"miles per minute(mpm)");
	}
}